#!/usr/bin/env python3

import cocotb
from cocotb.triggers import Timer


def bits_to_int(bitstr: str) -> int:
    return int(bitstr.replace("_", ""), 2)


@cocotb.test()
async def test_mips_rom(dut):
    expected = {
        0:  "00100100000010000000000000000101",  # addiu $t0, $0, 5
        1:  "00100100000010010000000000000011",  # addiu $t1, $0, 3
        2:  "00000001000010010101000000100001",  # addu  $t2, $t0, $t1
        3:  "00000001000010010101100000100011",  # subu  $t3, $t0, $t1
        4:  "00000001000010010110000000100100",  # and   $t4, $t0, $t1
        5:  "00000001000010010110100000100101",  # or    $t5, $t0, $t1
        6:  "00000001000010010111000000100110",  # xor   $t6, $t0, $t1
        7:  "00000001000010010111100000100111",  # nor   $t7, $t0, $t1
        8:  "10101100000010100000000000000000",  # sw    $t2, 0($0)
        9:  "10101100000010110000000000000100",  # sw    $t3, 4($0)
        10: "10001100000100000000000000000000",  # lw    $s0, 0($0)
        11: "10001100000100010000000000000100",  # lw    $s1, 4($0)
        12: "00000010000100011001000000100001",  # addu  $s2, $s0, $s1
        13: "00010010000100000000000000000001",  # beq   $s0, $s0, +1
        14: "00100100000010001101111010101101",  # addiu $t0, $0, 0xDEAD (nao executa)
        15: "00010101000010010000000000000001",  # bne   $t0, $t1, +1
        16: "00100100000010001011111011101111",  # addiu $t0, $0, 0xBEEF (nao executa)
        17: "00000000000010100100000001000000",  # sll   $t0, $t2, 1  -- funct=000000, shamt=00001
        18: "00000000000010000100000010000010",  # srl   $t0, $t0, 2
        19: "00000001001010100100000000101011",  # sltu  $t0, $t1, $t2
        20: "00000001001010100100000000100000",  # add   $t0, $t1, $t2  (bonus)
        21: "00000001010010010100000000100010",  # sub   $t0, $t2, $t1  (bonus)
        22: "00100000000010000000000000000111",  # addi  $t0, $0, 7     (bonus)
        23: "00000001001010100100000000101010",  # slt   $t0, $t1, $t2  (bonus)
        24: "00001000000000000000000000000000",  # j     0
    }

    # Verifica toda a ROM. Como addrWidth=6, são 64 endereços.
    for addr in range(2 ** len(dut.addr)):
        dut.addr.value = addr
        await Timer(1, units="ns")

        got = int(dut.data.value)
        exp = bits_to_int(expected.get(addr, "0" * 32))

        assert got == exp, (
            f"addr={addr}: esperado 0x{exp:08X}, obtido 0x{got:08X}"
        )
